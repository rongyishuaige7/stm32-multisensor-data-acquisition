static void draw_char(uint8_t x, uint8_t page, char ch)
{
    if (x > 122 || page > 7)
        return;
    uint8_t idx;
    if ((uint8_t)ch < 0x20 || (uint8_t)ch > 0x7E)
        idx = 0; /* 非 ASCII 用空格占位（避免中文等越界） */
    else
        idx = (uint8_t)ch - 0x20;
    const uint8_t *g = FONT6x8[idx];
    uint16_t base = (uint16_t)page * 128U + x;
    for (int i = 0; i < 6; i++) {
        s_buf[base + i] = g[i];
    }
}

void oled_draw_str(uint8_t x, uint8_t page, const char *s)
{
    while (*s && x <= 122) {
        draw_char(x, page, *s++);
        x += 6;
    }
}

/* ============ 对外接口 ============ */

static void gpio_init_pins(void)
{
    __HAL_RCC_GPIOB_CLK_ENABLE();
    GPIO_InitTypeDef g = {0};
    g.Mode = GPIO_MODE_OUTPUT_PP;
    g.Speed = GPIO_SPEED_FREQ_HIGH;
    g.Pull = GPIO_PULLUP;
    g.Pin = OLED_SCL_Pin;
    HAL_GPIO_Init(OLED_SCL_GPIO_Port, &g);
    g.Pin = OLED_SDA_Pin;
    HAL_GPIO_Init(OLED_SDA_GPIO_Port, &g);
    scl_write(1);
    sda_write(1);
}

void oled_clear(void)
{
    memset(s_buf, 0, sizeof(s_buf));
}

void oled_show(void)
{
    for (uint8_t page = 0; page < 8; page++) {
        ssd_cmd((uint8_t)(0xB0 | page)); /* page */
        ssd_cmd(0x00);                   /* 列低 */
        ssd_cmd(0x10);                   /* 列高 */
        ssd_data(&s_buf[(uint16_t)page * 128U], 128);
    }
}

void oled_init(void)
{
    gpio_init_pins();
    HAL_Delay(50);

    static const uint8_t init_seq[] = {
        0xAE,       /* display off */
        0x20, 0x02, /* page addressing */
        0xB0,
        0xC8,       /* COM scan reverse */
        0x00, 0x10, /* column start */
        0x40,       /* start line */
        0x81, 0x7F, /* contrast */
        0xA1,       /* seg remap */
        0xA6,       /* normal display */
        0xA8, 0x3F, /* mux 64 */
        0xA4,       /* resume from ram */
        0xD3, 0x00,
        0xD5, 0x80,
        0xD9, 0xF1,
        0xDA, 0x12,
        0xDB, 0x40,
        0x8D, 0x14,
        0xAF,
    };
    for (unsigned i = 0; i < sizeof(init_seq); i++)
        ssd_cmd(init_seq[i]);

    oled_clear();
    oled_show();
}

static void fmt_temp_c(char *out, size_t out_size, float temp_c)
{
    int32_t x100 = (int32_t)(temp_c * 100.0f + (temp_c >= 0.0f ? 0.5f : -0.5f));
    const char *sign = "";
    uint32_t abs_value;

    if (x100 < 0) {
        sign = "-";
        abs_value = (uint32_t)(-x100);
    } else {
        abs_value = (uint32_t)x100;
    }

    (void)snprintf(out, out_size, "%s%lu.%02lu",
                   sign,
                   (unsigned long)(abs_value / 100U),
                   (unsigned long)(abs_value % 100U));
}

void oled_render_snapshot(const sensor_snapshot_t *snap, bool wifi_online)
{
    if (!snap)
        return;
    char line[24];

    oled_clear();
    oled_draw_str(0, 0, "Sensor Monitor");
    oled_draw_str(96, 0, wifi_online ? " NET" : " ---");

    if (snap->temp_c > -900.0f) {
        char temp[16];
        fmt_temp_c(temp, sizeof(temp), snap->temp_c);
        snprintf(line, sizeof(line), "TEMP:%s C", temp);
    } else {
        snprintf(line, sizeof(line), "TEMP: ---- C");
    }
    oled_draw_str(0, 2, line);

    snprintf(line, sizeof(line), "RPM:%5u", (unsigned)snap->rpm);
    oled_draw_str(0, 3, line);

    snprintf(line, sizeof(line), "PRESS:%5u g", (unsigned)snap->pressure_g);
    oled_draw_str(0, 4, line);

    snprintf(line, sizeof(line), "Light:%s", snap->light_on ? "ON " : "OFF");
    oled_draw_str(0, 5, line);

    {
        const char *ip = esp_uart_get_ip();
        if (ip && ip[0]) {
            snprintf(line, sizeof(line), "IP:%s", ip);
        } else {
            snprintf(line, sizeof(line), "IP: ---");
        }
        oled_draw_str(0, 6, line);
    }

    snprintf(line, sizeof(line), "ALM:0x%02X Up:%lus",
             (unsigned)snap->alarm, (unsigned long)snap->uptime_s);
    oled_draw_str(0, 7, line);

    oled_show();
}
