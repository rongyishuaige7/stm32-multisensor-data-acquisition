/**
 * @file oled.c
 * @brief SSD1306 128x64 I2C OLED，软件 I2C 实现（PB6=SCL, PB7=SDA, 地址 0x3C）
 *
 * 依赖 HAL GPIO，不占用硬件 I2C 外设。
 * 帧缓冲 1KB，6x8 ASCII 字库（0x20..0x7E）。
 */

#include "oled.h"
#include "esp_uart.h"
#include "board_pins.h"
#include "stm32f1xx_hal.h"
#include <stdio.h>
#include <string.h>

/* ============ 软件 I2C 底层 ============ */

static void i2c_delay(void)
{
    /* ~1us 级延时，64MHz 下循环若干次即可，SSD1306 I2C 最快 400kHz 也够 */
    for (volatile int i = 0; i < 8; i++) {
        __NOP();
    }
}

static void scl_write(int v)
{
    HAL_GPIO_WritePin(OLED_SCL_GPIO_Port, OLED_SCL_Pin, v ? GPIO_PIN_SET : GPIO_PIN_RESET);
}

static void sda_write(int v)
{
    HAL_GPIO_WritePin(OLED_SDA_GPIO_Port, OLED_SDA_Pin, v ? GPIO_PIN_SET : GPIO_PIN_RESET);
}

static int sda_read(void)
{
    return HAL_GPIO_ReadPin(OLED_SDA_GPIO_Port, OLED_SDA_Pin) ? 1 : 0;
}

static void sda_mode(int out)
{
    GPIO_InitTypeDef g = {0};
    g.Pin = OLED_SDA_Pin;
    g.Speed = GPIO_SPEED_FREQ_HIGH;
    g.Pull = GPIO_PULLUP;
    /* 这里用推挽而不是真正的开漏，因为 SSD1306 模块板上已经有上拉电阻，
     * MCU 推挽输出更干净；读 ACK 时切回上拉输入。 */
    g.Mode = out ? GPIO_MODE_OUTPUT_PP : GPIO_MODE_INPUT;
    HAL_GPIO_Init(OLED_SDA_GPIO_Port, &g);
}

static void i2c_start(void)
{
    sda_mode(1);
    sda_write(1);
    scl_write(1);
    i2c_delay();
    sda_write(0);
    i2c_delay();
    scl_write(0);
    i2c_delay();
}

static void i2c_stop(void)
{
    sda_mode(1);
    sda_write(0);
    i2c_delay();
    scl_write(1);
    i2c_delay();
    sda_write(1);
    i2c_delay();
}

static bool i2c_write_byte(uint8_t b)
{
    sda_mode(1);
    for (int i = 0; i < 8; i++) {
        sda_write((b & 0x80) ? 1 : 0);
        b <<= 1;
        i2c_delay();
        scl_write(1);
        i2c_delay();
        scl_write(0);
        i2c_delay();
    }
    /* 读 ACK */
    sda_mode(0);
    i2c_delay();
    scl_write(1);
    i2c_delay();
    int ack = sda_read();
    scl_write(0);
    sda_mode(1);
    i2c_delay();
    return ack == 0;
}

/* ============ SSD1306 命令 / 数据 ============ */

static void ssd_cmd(uint8_t c)
{
    i2c_start();
    (void)i2c_write_byte((uint8_t)(OLED_I2C_ADDR << 1)); /* write */
    (void)i2c_write_byte(0x00);                          /* Co=0, D/C#=0 */
    (void)i2c_write_byte(c);
    i2c_stop();
}

static void ssd_data(const uint8_t *d, uint16_t n)
{
    i2c_start();
    (void)i2c_write_byte((uint8_t)(OLED_I2C_ADDR << 1));
    (void)i2c_write_byte(0x40); /* Co=0, D/C#=1 */
    for (uint16_t i = 0; i < n; i++) {
        (void)i2c_write_byte(d[i]);
    }
    i2c_stop();
}

